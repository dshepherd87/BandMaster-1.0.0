    public InstrumentMaintenance(Instrument i){
        initComponents();
        this.i = i;
        InstrumentSubType s;
        for(int j = 0; j < subTypeBox.getItemCount(); j++){
            s = (InstrumentSubType)subTypeBox.getItemAt(j);
            if(s.getSubTypeId() == this.i.getSubType().getSubTypeId()){
                subTypeBox.setSelectedIndex(j);
                break;
            }
        }                             
        InstrumentType t;
        for(int j = 0; j < typeBox.getItemCount(); j++){
            t =(InstrumentType)typeBox.getItemAt(j);
            if(t.getTypeId() == this.i.getType().getTypeId()){
                typeBox.setSelectedIndex(j);
                break;
            }
        }
        serialNumberField.setText(this.i.getSerialNumber());
        makeField.setText(this.i.getMake());
        modelField.setText(this.i.getModel());
    }

