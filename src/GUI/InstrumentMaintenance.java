    private void saveRecordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveRecordButtonActionPerformed
        i.setSubType((InstrumentSubType)subTypeBox.getSelectedItem());
        i.setType((InstrumentType)typeBox.getSelectedItem());
        i.setSerialNumber(serialNumberField.getText());
        i.setMake(makeField.getText());
        i.setModel(modelField.getText());
        Personnel personnel = (Personnel)personnelBox.getSelectedItem();
        i.setPersonnelId(personnel.getPersonnelId());       
        
        if(i.getInstrumentId() != 0){
            System.out.println("Updating");
            if(i.update()){
                JOptionPane.showMessageDialog(this, "Record saved");
                super.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Record could not be saved.\n"
                        + "Check error log for details.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            if(i.add()){
                JOptionPane.showMessageDialog(this, "Record created");
                super.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Record could not be created.\n"
                        + "Make sure this is not a duplicate.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }        
        }
    }//GEN-LAST:event_saveRecordButtonActionPerformed
    private DefaultComboBoxModel buildPersonnelModel(){     
        List<Personnel> p = Personnel.getList();
        p.add(0, new Personnel(0, "", "", "", "Unassigned", ""));
        Object[] b = p.toArray();
        return new DefaultComboBoxModel(b);
    }
